# "Red", "Orange", "Yellow", "Green", "Blue", "Indigo", "Violet"
gamma = int(input())

if 620 <= gamma <= 780:
    print("Red")
elif 590 <= gamma < 620:
    print("Orange")
elif 570 <= gamma < 590:
    print("Yellow")
elif 495 <= gamma < 570:
    print("Green")
elif 450 <= gamma < 495:
    print("Blue")
elif 425 <= gamma < 450:
    print("Indigo")
else:
    print("Violet")





